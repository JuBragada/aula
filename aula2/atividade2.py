valor = float(input("Informe um valor: "))
dobro = valor * 2
triplo = valor * 3
quadrado = valor ** 2
raiz = valor **(1/2)
raiz_cubica = valor **(1/3)
print(f'O dobro é {dobro:.0f}, o triplo é {triplo:.0f}, o valor ao quadrado é {quadrado:.0f}, a raiz quadrada é {raiz:.0f}, a raiz cúbica é {raiz_cubica:.2f}  ')